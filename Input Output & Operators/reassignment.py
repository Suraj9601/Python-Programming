# 44. Demonstrate variable reassignment.

a = 10         # integer
print("Initialize value of a : ", a)

a = 3.14       # float
print("After reassignment to float a : ", a)

a = "Hello"    # string
print("After reassignment to string a : ", a)

a = [1, 2, 3, 4, 5]     # List
print("After reassignment to list a : ", a)
